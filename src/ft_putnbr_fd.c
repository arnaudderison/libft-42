void	ft_putnbr_fd(int n, int fd)
{
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10);
	}
	ft_putchar_fd(48 + nb % 10, fd);
}